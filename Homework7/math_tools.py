##Takes arguments a and b (could be integers or floats) and returns their sum
def add(a, b):
	return a + b

##Takes arguments a and b and returns their difference
def substract(a, b):
	return a - b

##Takes arguments a and b and returns their product
def multiply(a, b):
	return a * b

##Takes arguments a and b and returns the quotient of a/b
def divide(a, b):
	return a / b
